// One-off codegen: turns fixtures/pointers/country_pointers.csv (community
// reference data, see that file's own header) into a static TS lookup so
// the UI can show "Argentina" instead of a raw numeric country code
// without adding an IPC round-trip for what's small, static reference data.
// Re-run manually if country_pointers.csv ever changes:
//   node ui/scripts/gen-country-names.mjs
import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const here = dirname(fileURLToPath(import.meta.url));
const csvPath = join(here, "..", "..", "fixtures", "pointers", "country_pointers.csv");
const outPath = join(here, "..", "src", "lib", "countryNames.ts");

const lines = readFileSync(csvPath, "utf-8").split(/\r?\n/);
const entries = [];
for (const line of lines) {
  if (!line || line.startsWith("#") || line.startsWith("pointer_hex")) continue;
  const [hex, name] = line.split(",");
  if (!hex || !name) continue;
  entries.push([parseInt(hex, 16), name]);
}
entries.sort((a, b) => a[0] - b[0]);

const body = entries.map(([code, name]) => `  ${code}: ${JSON.stringify(name)},`).join("\n");
const out = `// Generated by ui/scripts/gen-country-names.mjs from
// fixtures/pointers/country_pointers.csv -- do not hand-edit, re-run the
// script instead. Community reference data (not proprietary game
// content), keyed by the same numeric code stored in Team.country /
// Player.nationality / Player.birthCountry.
const COUNTRY_NAMES: Record<number, string> = {
${body}
};

/** Falls back to the raw numeric code (as a string) for an unknown byte. */
export function countryName(code: number): string {
  return COUNTRY_NAMES[code] ?? String(code);
}
`;
writeFileSync(outPath, out);
console.log(`wrote ${entries.length} countries to ${outPath}`);
